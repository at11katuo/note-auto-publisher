import type { Idea } from '@note/shared'

export type DirectorOutput = {
  outline: {
    sections: Array<{ heading: string; points: string[] }>
  }
  image_prompt: string
  key_facts?: string[]
}

export function buildWriterPrompt(outline: DirectorOutput, idea: Idea): string {
  const sectionsText = outline.outline.sections
    .map(
      (s, i) =>
        `【第${i + 1}章: ${s.heading}】\n${s.points.map((p) => `・${p}`).join('\n')}`,
    )
    .join('\n\n')

  const factsText =
    (outline.key_facts ?? []).length > 0
      ? (outline.key_facts ?? []).map((f) => `・${f}`).join('\n')
      : '(なし)'

  return `以下の構成案と参考事実に基づいて、note に投稿する記事を書いてください。

【ネタ情報】
タイトル: ${idea.title}
要約: ${idea.summary}
ソース: ${idea.sourceUrl ?? '(なし)'}

【記事構成案（AIディレクターが最新検索に基づき作成）】
${sectionsText}

【参考事実・最新データ（ウェブ検索から収集済み）】
${factsText}

【出力フォーマット】
以下の JSON 形式で厳密に返してください。前置きや説明文は一切不要、JSON のみを出力してください。

{
  "title": "● 記事タイトル(30〜45字、煽りなし、検索を意識)",
  "tags": ["タグ1", "タグ2", ...],
  "body": "記事本文(1500字程度)"
}

【ターゲット読者と文体】
- ターゲット: 投資を始めたばかりの初心者〜興味を持ち始めた人
- 専門用語はできるだけ避け、使う場合は必ずカッコ書きで短く説明を添える（例: ETF（複数の株をまとめて買える投資商品）など）
- 丁寧語（ですます調）で書く。語りかけるような親しみやすいトーンを意識する
- 難しい概念は身近な例え話に置き換えて説明する

【記事構成（厳守）】
記事は必ず以下の5ブロック構成で書くこと。見出しは3つに固定し、増やしてはならない。

  ① 導入文     ：150〜200字
  ② 見出し1本文：200〜400字（絶対に400字を超えないこと）
  ③ 見出し2本文：200〜400字（絶対に400字を超えないこと）
  ④ 見出し3本文：200〜400字（絶対に400字を超えないこと）
  ⑤ まとめ文   ：150〜200字

合計1,500字前後に美しく収める。各ブロックを書き終えたら文字数を意識してボリュームを調整すること。

【記事執筆の指示】
- 見出し記号（## や #）は一切使わない。流れるような文章で書く
- アスタリスク（* ＊ ** ）は本文中で絶対に使用しない。強調したい語句は「」や【】で括る。箇条書きには・か数字を使う
- title フィールドの先頭は必ず「● 」（黒丸＋半角スペース）で始める
- 導入: 自分がこの話題をどう受け止めたか、個人的な文脈から入る
- 見出し1〜3: 構成案の各章を順に展開する。参考事実・最新データを初心者にもわかるよう噛み砕いて伝える
- まとめ: 「結局どうするか」自分の方針を短く締める
- 段落間は空行を入れて読みやすく
- 自分の保有資産（約1500万円、オルカン積立中）への具体的な影響・感想を必ず1箇所以上入れる
- 最後の免責文はシステム側で挿入するので不要`
}
