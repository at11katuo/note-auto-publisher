import type { Idea } from '@note/shared'

export function buildArticlePrompt(idea: Idea, feedback?: string): string {
  const feedbackSection = feedback?.trim()
    ? `\n\n【前回の記事への指摘・修正依頼】\n以下の指摘を反映して記事を書き直してください:\n${feedback.trim()}`
    : ''

  return `以下のネタを元に、note に投稿する記事を書いてください。${feedbackSection}

【ネタ情報】
タイトル: ${idea.title}
要約: ${idea.summary}
ソース: ${idea.sourceUrl ?? '(なし)'}
本文抜粋: ${idea.rawContent ?? '(なし)'}

【出力フォーマット】
以下の JSON 形式で厳密に返してください。前置きや説明文は一切不要、JSON のみを出力してください。

{
  "title": "記事タイトル(30〜45字、煽りなし、検索を意識)",
  "tags": ["タグ1", "タグ2", ...],
  "body": "記事本文(1500字程度)"
}

【ターゲット読者と文体】
- ターゲット: 投資を始めたばかりの初心者〜興味を持ち始めた人
- 専門用語はできるだけ避け、使う場合は必ずカッコ書きで短く説明を添える
- 丁寧語（ですます調）で書く。語りかけるような親しみやすいトーンを意識する
- 難しい概念は身近な例え話に置き換えて説明する

【記事構成の指示】
- 見出し記号（## や # ）は一切使わない。流れるような文章で書く
- 文字数は1500字程度。冗長にならず簡潔にまとめる
- 冒頭: 自分がこのニュース・話題を見てどう感じたか、個人的な文脈から入る（100〜150字）
- 中盤: 話題の背景・内容の解説 → 初心者にもわかるよう噛み砕いて → 自分の積立への影響
- 末尾: 「結局どうするか」自分の方針を短く締める（100〜150字）
- 段落間は適宜改行を入れて読みやすく
- 自分の経験や保有資産の数字を必ず1つ以上盛り込む
- 最後の免責文はシステム側で挿入するので不要`
}
