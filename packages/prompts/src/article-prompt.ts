import type { Idea } from '@note/shared'

export function buildArticlePrompt(idea: Idea): string {
  return `以下のネタを元に、note に投稿する記事を書いてください。

【ネタ情報】
タイトル: ${idea.title}
要約: ${idea.summary}
ソース: ${idea.sourceUrl ?? '(なし)'}
本文抜粋: ${idea.rawContent ?? '(なし)'}

【出力フォーマット】
以下の JSON 形式で厳密に返してください。前置きや説明文は一切不要、JSON のみを出力してください。

{
  "title": "記事タイトル(30〜45字、煽りなし、検索を意識)",
  "tags": ["タグ1", "タグ2", ...],
  "body": "記事本文(2000〜3500字)"
}

【記事構成の指示】
- 見出し記号（## や # ）は一切使わない。流れるような文章で書く
- 冒頭: 自分がこのニュース・話題を見てどう感じたか、個人的な文脈から入る（150〜250字）
- 中盤: 話題の背景・内容の解説 → 長期投資家としての自分の受け止め方 → 具体的な数字や自分の積立への影響
- 末尾: 「結局どうするか」自分の方針を短く締める（150〜200字）
- 段落間は適宜改行を入れて読みやすく
- 自分の経験や保有資産の数字を必ず1つ以上盛り込む
- 最後の免責文はシステム側で挿入するので不要`
}
